#include "3-calc.h"

/**
 * op_add - a function that sum two numbers
 * @a: first number
 * @b: second number
 *
 * Return:  the sum
*/
	int op_add(int a, int b)
	{
		return (a + b);
	}

/**
 * op_sub - a function that subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return:  the substraction
*/
	int op_sub(int a, int b)
	{
		return (a - b);
	}

/**
 * op_mul - a function that multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return:  the multiplication
*/
	int op_mul(int a, int b)
	{
		return (a * b);
	}

/**
 * op_div - a function that divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return:  the division
*/
	int op_div(int a, int b)
	{
		return (a / b);
	}

/**
 * op_mod - a function that modules two numbers
 * @a: first number
 * @b: second number
 *
 * Return:  the modulus
*/
	int op_mod(int a, int b)
	{
		return (a % b);
	}
